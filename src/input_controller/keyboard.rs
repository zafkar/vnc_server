use xkeysym::Keysym;

pub fn xkeysym_into_enigo(key: Keysym) -> enigo::Key {
    if let Some(unicode) = key.key_char() {
        return enigo::Key::Unicode(unicode);
    }

    match key {
        Keysym::KP_Add => enigo::Key::Add,
        Keysym::Alt_L => enigo::Key::Alt,
        Keysym::BackSpace => enigo::Key::Backspace,
        Keysym::Begin => enigo::Key::Begin,
        Keysym::Break => enigo::Key::Break,
        Keysym::Cancel => enigo::Key::Cancel,
        Keysym::Caps_Lock => enigo::Key::CapsLock,
        Keysym::Clear => enigo::Key::Clear,
        Keysym::Control_L => enigo::Key::Control,
        Keysym::KP_Decimal => enigo::Key::Decimal,
        Keysym::Delete => enigo::Key::Delete,
        Keysym::KP_Divide => enigo::Key::Divide,
        Keysym::Down => enigo::Key::DownArrow,
        Keysym::End => enigo::Key::End,
        Keysym::Escape => enigo::Key::Escape,
        Keysym::Execute => enigo::Key::Execute,
        Keysym::F1 => enigo::Key::F1,
        Keysym::F2 => enigo::Key::F2,
        Keysym::F3 => enigo::Key::F3,
        Keysym::F4 => enigo::Key::F4,
        Keysym::F5 => enigo::Key::F5,
        Keysym::F6 => enigo::Key::F6,
        Keysym::F7 => enigo::Key::F7,
        Keysym::F8 => enigo::Key::F8,
        Keysym::F9 => enigo::Key::F9,
        Keysym::F10 => enigo::Key::F10,
        Keysym::F11 => enigo::Key::F11,
        Keysym::F12 => enigo::Key::F12,
        Keysym::F13 => enigo::Key::F13,
        Keysym::F14 => enigo::Key::F14,
        Keysym::F15 => enigo::Key::F15,
        Keysym::F16 => enigo::Key::F16,
        Keysym::F17 => enigo::Key::F17,
        Keysym::F18 => enigo::Key::F18,
        Keysym::F19 => enigo::Key::F19,
        Keysym::F20 => enigo::Key::F20,
        Keysym::F21 => enigo::Key::F21,
        Keysym::F22 => enigo::Key::F22,
        Keysym::F23 => enigo::Key::F23,
        Keysym::F24 => enigo::Key::F24,
        Keysym::F25 => enigo::Key::F25,
        Keysym::F26 => enigo::Key::F26,
        Keysym::F27 => enigo::Key::F27,
        Keysym::F28 => enigo::Key::F28,
        Keysym::F29 => enigo::Key::F29,
        Keysym::F30 => enigo::Key::F30,
        Keysym::F31 => enigo::Key::F31,
        Keysym::F32 => enigo::Key::F32,
        Keysym::F33 => enigo::Key::F33,
        Keysym::F34 => enigo::Key::F34,
        Keysym::F35 => enigo::Key::F35,
        Keysym::Find => enigo::Key::Find,
        Keysym::Hangul => enigo::Key::Hangul,
        Keysym::Hangul_Hanja => enigo::Key::Hanja,
        Keysym::Help => enigo::Key::Help,
        Keysym::Home => enigo::Key::Home,
        Keysym::Insert => enigo::Key::Insert,
        Keysym::Kanji => enigo::Key::Kanji,
        Keysym::Left => enigo::Key::LeftArrow,
        Keysym::Linefeed => enigo::Key::Linefeed,
        Keysym::Menu => enigo::Key::LMenu,
        Keysym::Mode_switch => enigo::Key::ModeChange,
        Keysym::KP_Multiply => enigo::Key::Multiply,
        Keysym::XF86_AudioNext => enigo::Key::MediaNextTrack,
        Keysym::XF86_AudioPlay => enigo::Key::MediaPlayPause,
        Keysym::XF86_AudioPrev => enigo::Key::MediaPrevTrack,
        Keysym::XF86_AudioStop => enigo::Key::MediaStop,
        Keysym::Num_Lock => enigo::Key::Numlock,
        Keysym::KP_0 => enigo::Key::Numpad0,
        Keysym::KP_1 => enigo::Key::Numpad1,
        Keysym::KP_2 => enigo::Key::Numpad2,
        Keysym::KP_3 => enigo::Key::Numpad3,
        Keysym::KP_4 => enigo::Key::Numpad4,
        Keysym::KP_5 => enigo::Key::Numpad5,
        Keysym::KP_6 => enigo::Key::Numpad6,
        Keysym::KP_7 => enigo::Key::Numpad7,
        Keysym::KP_8 => enigo::Key::Numpad8,
        Keysym::KP_9 => enigo::Key::Numpad9,
        Keysym::Page_Down => enigo::Key::PageDown,
        Keysym::Page_Up => enigo::Key::PageUp,
        Keysym::Pause => enigo::Key::Pause,
        Keysym::Print => enigo::Key::PrintScr,
        Keysym::Control_R => enigo::Key::RControl,
        Keysym::Redo => enigo::Key::Redo,
        Keysym::Return => enigo::Key::Return,
        Keysym::Right => enigo::Key::RightArrow,
        Keysym::Shift_R => enigo::Key::RShift,
        Keysym::Scroll_Lock => enigo::Key::ScrollLock,
        Keysym::Select => enigo::Key::Select,
        // Keysym::script_switch => enigo::Key::ScriptSwitch,
        Keysym::Shift_L => enigo::Key::LShift,
        Keysym::Shift_Lock => enigo::Key::ShiftLock,
        Keysym::space => enigo::Key::Space,
        Keysym::KP_Subtract => enigo::Key::Subtract,
        Keysym::Sys_Req => enigo::Key::SysReq,
        Keysym::Tab => enigo::Key::Tab,
        Keysym::Undo => enigo::Key::Undo,
        Keysym::Up => enigo::Key::UpArrow,
        Keysym::XF86_AudioLowerVolume => enigo::Key::VolumeDown,
        Keysym::XF86_AudioRaiseVolume => enigo::Key::VolumeUp,
        Keysym::XF86_AudioMute => enigo::Key::VolumeMute,
        Keysym::XF86_AudioMicMute => enigo::Key::MicMute,
        Keysym::Super_L => enigo::Key::Meta,
        other => enigo::Key::Other(other.raw()),
    }
}
